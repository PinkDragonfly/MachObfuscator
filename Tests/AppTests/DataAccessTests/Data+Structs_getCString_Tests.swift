@testable import App
import XCTest

class Data_Structs_getCString_Tests: XCTestCase {
    let data = Data([0x64, 0x6F, 0x6E, 0x27, 0x74, 0x20, 0x6F, 0x62, 0x66, 0x75, 0x73, 0x63, 0x61, 0x74, 0x65, 0x20, 0x6D, 0x65, 0x00, 0x6D, 0x65, 0x6D, 0x6F, 0x72, 0x79, 0x20, 0x63, 0x6F, 0x72, 0x72, 0x75, 0x70, 0x74, 0x69, 0x6F, 0x6E])

    func test_shouldReadCString() {
        // Expect
        XCTAssertEqual(data.getCString(atOffset: 6),
                       "obfuscate me")
    }

    func test_shouldReadEmptyCString() {
        // Expect
        XCTAssertEqual(data.getCString(atOffset: 18),
                       "")
    }
}
